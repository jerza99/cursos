
    let a = 10;
    console.log(a > 10); // a es mayor que 10  es verdadero
    console.log(a >= 10); // a es mayor o igual que 10  es verdadero

    console.log(a < 10); // a es menor que 10  es falso
    console.log(a <= 10); // a es menor o igual que 10  es falso

    console.log(a == 10); // igualdad
    console.log(a != 10); // distinto
    console.log(a === 10); // saber si es el mimo tipo y si es el mismo valor
    console.log(a !== 10); // saber si es distinto tipo y  valor
